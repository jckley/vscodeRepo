items = [1, 2, 3, 4, 5, 6]

# print with [] brackets separated by ','
print(items)

# print using operator
print(*items)

# print using for
for i in items:
    print(i)

# print using for and len function
for i in range(len(items)):
    print(items[i])

# you can also add sep and end methods to the print statement accordingly

